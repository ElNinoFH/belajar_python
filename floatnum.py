# float adalah angka yang mengandung decimal
number1 = 15.5
print(type(number1))
print(number1)

# float bisa memiliki lebih dari satu angka decimal
number2 = 15.50703
print(type(number2))
print(number2)

# float bisa berupa angka positif atau negatif
number3 = -15.5
print(type(number3))
print(number3)