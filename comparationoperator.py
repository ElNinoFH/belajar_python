number1 = 14
number2 = 6

# == (sama dengan)
print(number1 == number2)

# != (tidak sama dengan)
print(number1 != number2)

# > (lebih dari)
print(number1 > number2)

# < (kurang dari)
print(number1 < number2)

# >= (lebih atau sama dengan)
print(number1 >= number2)

# <= (kurang atau sama dengan)
print(number1 <= number2)